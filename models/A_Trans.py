class SpatialAttention(nn.Module):
    """参考 CBAM 的空间注意力机制"""
    def __init__(self):
        super().__init__()
        self.conv = nn.Sequential(
            nn.Conv2d(2, 1, kernel_size=7, padding=3, bias=False),
            nn.Sigmoid()
        )

    def forward(self, x):
        avg_out = torch.mean(x, dim=1, keepdim=True)
        max_out = torch.max(x, dim=1, keepdim=True)[0]
        attn = torch.cat([avg_out, max_out], dim=1)
        return x * self.conv(attn)

class DetailAwareAttention(nn.Module):
    def __init__(self, channels):
        super().__init__()
        assert channels % 3 == 0, "channels is must 3n"

        # 边缘检测分支
        self.edge = nn.Sequential(
            nn.Conv2d(channels, channels, 3, padding=1, groups=channels, bias=False),
            nn.InstanceNorm2d(channels),
            nn.ReLU(inplace=True)
        )

        # 多尺度上下文分支 + 空间注意力
        self.context = nn.ModuleList([
            nn.Sequential(
                nn.Conv2d(channels, channels // 3, 3, padding=d, dilation=d, bias=False),
                nn.BatchNorm2d(channels // 3),
                nn.ReLU(inplace=True),
                SpatialAttention()
            ) for d in [1, 2, 3]
        ])

        # 通道注意力（CBAM-like）
        self.channel_att = nn.Sequential(
            nn.AdaptiveAvgPool2d(1),
            nn.Conv2d(channels * 2, channels // 12, 1, bias=False),
            nn.ReLU(inplace=True),
            nn.Conv2d(channels // 12, channels * 2, 1, bias=False),
            nn.Sigmoid()
        )

        # 融合
        self.fusion = nn.Sequential(
            nn.Conv2d(channels * 2, channels, 1, bias=False),
            nn.BatchNorm2d(channels),
            nn.ReLU(inplace=True)
        )

    def forward(self, x):
        edge = self.edge(x)

        context_feats = [branch(x) for branch in self.context]  # list of (B, C//3, H, W)
        context = torch.cat(context_feats, dim=1)  # (B, C, H, W)

        combined = torch.cat([edge, context], dim=1)  # (B, 2C, H, W)
        weight = self.channel_att(combined)
        combined = combined * weight

        fused = self.fusion(combined)
        return fused + x  # 残差连接
